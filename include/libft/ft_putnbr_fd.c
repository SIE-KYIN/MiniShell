#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	buf[11];

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		write(fd, "-", 1);
		n = n * -1;
	}
	i = -1;
	if (n == 0)
		write(fd, "0", 1);
	while (n > 0)
	{
		buf[++i] = (n % 10 + 48);
		n = n / 10;
	}
	while (i >= 0)
	{
		write(fd, &buf[i--], 1);
	}
}
